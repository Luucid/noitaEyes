    yc = 8
    
    # color = {'.r':0, '.y':1, '.c':2, '.m':3, 'ok':4 }
    color = {'.b':0, '^k':1, '>k':2, 'vk':3, 'ok':4 }
    clr = list(color.keys())
    alphaReset = alp
    
    for i in table:
        xc = 0
        for j in i:
            alp = alphaReset
            if (not c and j == 0):
                alp = 0
            elif (not u and j == 1):
                alp = 0
            elif (not r and j == 2):
                alp = 0
            elif (not d and j == 3):
                alp = 0
            elif (not l and j == 4):
                alp = 0
            
            if(yc%2 != 0):
                plt.plot((xc+0.5), yc, clr[j], alpha=alp)
            else:
                plt.plot(xc, yc, clr[j], alpha=alp)
                
            xc+=1
        yc-=1




